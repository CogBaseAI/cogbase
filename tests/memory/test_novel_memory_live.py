# A distilled subject-matter belief is durable when it lands as a FACT or a
# CORRECTION (PREFERENCE / RETRIEVAL_HINT are interaction memory, not the
# subject-matter knowledge this measurement is about).
_DURABLE_FACT_KINDS = frozenset({MemoryKind.FACT.value, MemoryKind.CORRECTION.value})

# Experiment: an application-specific topic scope handed to the distiller's
# extraction prompt (Distiller(domain_fact_guidance=...)).  It narrows which
# subject-matter facts of *this* corpus — Le Plongeon's "Vestiges of the Mayas"
# — count as durable, without touching the provenance rule.  Set to ``None`` to
# A/B against the generic prompt.
_DOMAIN_FACT_GUIDANCE = (
    "This application answers questions about Augustus Le Plongeon's scholarly "
    "work 'Vestiges of the Mayas', concerning the ancient Maya civilization of "
    "Yucatan. Durable subject-matter facts are concrete, reusable claims the "
    "work makes that a future question could turn on:\n"
    "- geography and places (e.g. Yucatan, Uxmal, Chichen-Itza, Tierra de "
    "Guerra, Mayab) and what is located at, found in, or significant about "
    "them;\n"
    "- named peoples and persons (e.g. the Mayas, Carians, Egyptians, Augustus "
    "and Mrs. Le Plongeon, Chaacmol) and their roles, customs, or "
    "contributions;\n"
    "- asserted connections or similarities BETWEEN civilizations (shared "
    "customs, religious symbols, architecture, language) — bind the specific "
    "shared trait to the specific peoples it links;\n"
    "- archaeological evidence and the specific monument, inscription, or "
    "artifact it rests on.\n"
    "Prefer facts that bind a specific named entity to a specific attribute, "
    "location, or relationship. Do not distill vague thematic impressions or "
    "whole-passage summaries."
)


def _user_fact(q: dict) -> str:
    """Phrase a benchmark Q&A as a fact the USER asserts and asks to remember.

    Routes the ground-truth fact through the user channel — the provenance the
    distiller treats as durable — instead of an assistant answer the model
    retrieved from the corpus (which the provenance rule declines).
    """
    return (
        "Please remember this for future questions. "
        f'Regarding "{q["question"]}", the established fact is: {q["answer"]}'
    )


async def _new_session(
    env: _NovelEnv, name: str
) -> tuple[LongTermMemory, EpisodicMemory, str]:
    """A fresh app-scoped long-term store + an episodic session bound to it."""
    lt = _fresh_long_term(env, name)
    await lt.setup()
    episodic = EpisodicMemory(env.log)
    short_term = ShortTermMemory(episodic=episodic, llm=_llm)
    sid = await short_term.start_session(app_id=name)
    episodic.bind_app(sid, app_id=name)
    return lt, episodic, sid


async def _distill_accept_and_collect(
    lt: LongTermMemory, episodic: EpisodicMemory, sid: str, name: str
) -> list[dict]:
    """Distil the session, accept any pending facts, return the durable records.

    FACT/CORRECTION distilled here land at ``pending_review`` and are never
    recalled until a reviewer accepts them — the same gate the API exposes via
    ``list_pending_memories`` / ``review_memories``.  Drive that gate with the
    low-level review APIs so accepted facts become recallable, rather than
    monkeypatching the auto-activate set.  Returns the active durable-kind
    records (the provenance rule's observable output).
    """
    distiller = Distiller(episodic, lt, _llm, domain_fact_guidance=_DOMAIN_FACT_GUIDANCE)
    memory_ids = await distiller.distill_session(session_id=sid)
    pending = await lt.list_pending(limit=MAX_REVIEW_BATCH)
    if pending:
        await lt.review_many(
            decisions=[ReviewDecision(memory_id=r.memory_id, accept=True) for r in pending]
        )
    active = await lt._structured.query(  # noqa: SLF001 — verifying store contents
        lt._structured_collection, [Col("status") == MemoryStatus.ACTIVE.value]
    )
    durable = [r for r in active if r.get("kind") in _DURABLE_FACT_KINDS]
    logger.info(
        "[answer-eval] %s: distilled %d candidate(s), accepted %d pending, "
        "%d active record(s) (%d durable fact)",
        name, len(memory_ids), len(pending), len(active), len(durable),
    )
    return durable


async def _seed_user_facts_and_distill(
    env: _NovelEnv, name: str, statements: list[str]
) -> tuple[LongTermMemory, list[dict]]:
    """Provenance-positive: the USER asserts each fact; distil + accept it."""
    lt, episodic, sid = await _new_session(env, name)
    for statement in statements:
        await episodic.record_user_message(session_id=sid, content=statement)
    await episodic.flush(sid)
    return lt, await _distill_accept_and_collect(lt, episodic, sid, name)


async def _seed_assistant_answers_and_distill(
    env: _NovelEnv, name: str, qa_pairs: list[tuple[str, str]]
) -> tuple[LongTermMemory, list[dict]]:
    """Provenance-negative: the assistant *answers* each question (retrieved from
    the corpus); the distiller should decline these as durable facts."""
    lt, episodic, sid = await _new_session(env, name)
    for question, answer in qa_pairs:
        await episodic.record_user_message(session_id=sid, content=question)
        await episodic.record_final_answer(session_id=sid, answer=answer)
    await episodic.flush(sid)
    return lt, await _distill_accept_and_collect(lt, episodic, sid, name)



class TestLongTermLive:
    async def test_promote_recall_and_memory_block_injection(self, env):
        lt = env.long_term
        await lt.setup()
        # A preference auto-promotes to active (the gate for low-risk signals).
        await lt.promote(
            candidate=MemoryCandidate(
                content=(
                    "The user is studying Maya archaeology and prefers concise "
                    "answers that name the archaeological site first."
                ),
                kind=MemoryKind.PREFERENCE,
                entities=["maya archaeology"],
                confidence=0.7,
            )
        )

        recalled = await lt.recall(query="how should answers about Maya sites be formatted?")
        assert any("concise" in r.content.lower() for r in recalled), (
            f"expected the preference to be recalled; got {[r.content for r in recalled]}"
        )

        # Wired into the runner, recall injects a memory-derived system block.
        runner = env.runner(with_long_term=True)
        captured: list = []
        orig = _llm.complete_stream

        def _spy(messages, *a, **kw):
            captured.append(messages)
            return orig(messages, *a, **kw)

        _llm.complete_stream = _spy
        try:
            await _query(runner, "How do I like my answers about Maya sites?")
        finally:
            _llm.complete_stream = orig

        system_blocks = [
            m["content"] for msgs in captured for m in msgs if m.get("role") == "system"
        ]
        assert any("memory-derived" in b and "concise" in b for b in system_blocks), (
            "expected a memory-derived system block carrying the preference"
        )

    async def test_reconcile_duplicate_with_real_llm(self, env):
        # A fresh long-term store so this test's reconciliation is isolated.
        persist = Path(env.log._root).parent / "reconcile"
        lt = LongTermMemory(
            SQLiteStructuredStore(persist / "lt.sqlite").with_scope(AppScope(app_id="recon")),
            FAISSVectorStore(path=persist / "lt_faiss").with_scope(AppScope(app_id="recon")),
            _llm,
            _embedder,
            app_id="recon",
        )
        await lt.setup()

        first = await lt.promote(
            candidate=MemoryCandidate(
                content="The user is researching the ancient Maya civilization.",
                kind=MemoryKind.PREFERENCE,
                entities=["maya"],
                confidence=0.7,
            )
        )
        # A paraphrase of the same claim — the real LLM should UPDATE/NOOP, not
        # add a contradiction, so the store stays curated (no duplicate ADD that
        # supersedes the first).
        affected = await lt.reconcile(
            candidate=MemoryCandidate(
                content="The user studies the ancient Maya people and their history.",
                kind=MemoryKind.PREFERENCE,
                entities=["maya"],
                confidence=0.7,
            )
        )
        assert affected, "reconcile returned no affected id"

        active = await lt._structured.query(  # noqa: SLF001 — verifying store contents
            lt._structured_collection, [Col("status") == MemoryStatus.ACTIVE.value]
        )
        assert len(active) >= 1
        # The original belief survives (reinforced or kept), not superseded away.
        assert any(r["memory_id"] == first for r in active) or affected == first, (
            "the duplicate observation should not have superseded the original belief"
        )

    async def test_distill_session_into_long_term_with_real_llm(self, env):
        # A short session that states a durable preference worth distilling.
        episodic = EpisodicMemory(env.log)
        short_term = ShortTermMemory(episodic=episodic, llm=_llm)
        long_term = LongTermMemory(
            SQLiteStructuredStore(Path(env.log._root).parent / "distill_lt.sqlite").with_scope(
                AppScope(app_id="distill")
            ),
            FAISSVectorStore(path=Path(env.log._root).parent / "distill_lt_faiss").with_scope(
                AppScope(app_id="distill")
            ),
            _llm,
            _embedder,
            app_id="distill",
        )
        await long_term.setup()

        sid = await short_term.start_session(app_id="distill")
        runner = QueryRunner(
            app_id="distill",
            llm=_llm,
            resources=RetrievalResources(
                document_store=env.docs,
                vector_store=env.vector,
                embedder=_embedder,
                vector_schemas=[env.vector_schema],
            ),
            memory=MemoryTiers(short_term=short_term, episodic=episodic),
        )
        await _query(
            runner,
            "Remember that I'm writing a thesis on Maya archaeology and I always "
            "want concise answers with the site name first. Which site has Maya "
            "inscriptions?",
            session_id=sid,
        )

        distiller = Distiller(episodic, long_term, _llm)
        memory_ids = await distiller.distill_session(session_id=sid)
        assert isinstance(memory_ids, list)

        # The distiller ran against the real log; whatever it promoted is in the
        # store. A preference about the thesis should be recoverable.
        all_rows = await long_term._structured.query(  # noqa: SLF001
            long_term._structured_collection
        )
        logger.info("[novel] distilled %d candidates, store has %d rows",
                    len(memory_ids), len(all_rows))
        assert all_rows, (
            "expected the distiller to extract at least one durable memory from a "
            "session that explicitly stated a stable preference"
        )


# ---------------------------------------------------------------------------
# Memory ↔ document conflict: which source wins, and is the conflict surfaced?
#
# The recalled-memory block carries an evidence-precedence policy (see
# query_runner._MEMORY_EVIDENCE_POLICY): a recalled memory is dated,
# user-attributed background, so on a conflict with what the documents currently
# say the model must prefer the document evidence (the fresher, authoritative
# source for subject-matter facts) and note the discrepancy — rather than
# silently echoing a stale memory.
#
# This drives that policy end-to-end against the real corpus.  The corpus says
# the site noted for its Maya inscriptions is Uxmal (the
# TestShortTermAndEpisodicLive turn-1 fact).  We seed an *active* long-term
# memory asserting a different site, then ask the question so recall injects the
# conflicting memory alongside the corpus.  The wrong site is Machu Picchu — an
# Inca site in Peru that this Yucatán-Maya novel never mentions — so the claim
# is a genuine contradiction the corpus cannot corroborate (unlike, say, Chichen
# Itza, which the corpus also credits with inscriptions).  That makes the
# discrepancy signal unambiguous: "Uxmal" in the answer can only come from the
# documents, and "Machu Picchu" can only come from the memory.  The test is
# non-vacuous: it first asserts the wrong memory actually reached the LLM
# context, then asserts the document fact still wins.
# ---------------------------------------------------------------------------


class TestMemoryDocumentConflictLive:
    async def test_document_wins_over_conflicting_memory_and_conflict_surfaced(self, env):
        questions = _load_questions()
        q = _find_question(questions, "noted for its maya inscriptions")
        assert "uxmal" in q["answer"].lower()  # the corpus / ground-truth fact

        lt = _fresh_long_term(env, "conflict")
        await lt.setup()
        # A subject-matter claim that contradicts the corpus.  Forced ACTIVE so it
        # is recallable (a FACT/CORRECTION would otherwise wait at pending_review),
        # framing it as a user-attributed belief the precedence policy must weigh.
        # Machu Picchu never appears in this corpus, so it is a clean contradiction.
        await lt.promote(
            candidate=MemoryCandidate(
                content=(
                    "The user noted that the site famous for its ancient Maya "
                    "inscriptions is Machu Picchu."
                ),
                kind=MemoryKind.CORRECTION,
                entities=["machu picchu", "maya inscriptions"],
                confidence=0.7,
            ),
            status=MemoryStatus.ACTIVE,
        )

        # Sanity: the wrong memory is recallable for this question's topic.
        recalled = await lt.recall(query=q["question"])
        assert any("machu picchu" in r.content.lower() for r in recalled), (
            f"expected the conflicting memory to be recalled; got "
            f"{[r.content for r in recalled]}"
        )

        runner = env.runner(long_term=lt)
        captured: list = []
        orig = _llm.complete_stream

        def _spy(messages, *a, **kw):
            captured.append(messages)
            return orig(messages, *a, **kw)

        _llm.complete_stream = _spy
        try:
            result = await _query_result(runner, q["question"])
        finally:
            _llm.complete_stream = orig
        answer = result.answer.lower()

        # Non-vacuity: the conflicting memory really reached the LLM context as a
        # memory-derived block (otherwise "documents win" would be trivial).
        system_blocks = [
            m["content"] for msgs in captured for m in msgs if m.get("role") == "system"
        ]
        assert any("machu picchu" in b.lower() and "memory-derived" in b for b in system_blocks), (
            "expected the conflicting memory injected as a memory-derived block"
        )
        assert any("machu picchu" in m.content.lower() for m in result.memories), (
            "the conflicting memory should be attached to the QueryResult"
        )

        # The precedence policy's deterministic guarantee: the document fact
        # (Uxmal) wins over the stale, user-attributed memory (Machu Picchu).
        assert "uxmal" in answer, (
            "the document evidence (Uxmal) should win over the conflicting "
            f"memory (Machu Picchu):\n{result.answer}"
        )
        # And the winning fact is grounded in retrieved document evidence, not the
        # memory: the QueryResult carries document references (vector_search chunks
        # and/or read_document slices), and at least one cites Uxmal.
        doc_refs = [c.text for c in result.chunks] + [s.text for s in result.document_slices]
        assert doc_refs, (
            "expected the answer to draw on document references (chunks/slices), "
            "confirming Uxmal came from the corpus rather than memory"
        )
        assert any("uxmal" in t.lower() for t in doc_refs), (
            "expected a retrieved document reference to ground the Uxmal answer"
        )
        # The memory must NOT have hijacked the answer: a conflicting memory that
        # is wrong should never be presented as the site.  (Machu Picchu is absent
        # from the corpus, so its only possible source here is the memory.)
        assert "machu picchu" not in answer or "uxmal" in answer, (
            f"the conflicting memory should not override the documents:\n{result.answer}"
        )
        # "Note the discrepancy" is the softer half of the policy and is
        # model-dependent — observed, not asserted.  Machu Picchu is absent from
        # the corpus, so if it surfaces at all it is the model flagging the
        # conflict; in practice the model often just drops an obviously-wrong
        # memory rather than naming it.
        surfaced = "machu picchu" in answer
        logger.info(
            "[conflict] documents won (Uxmal present); discrepancy surfaced=%s\n%s",
            surfaced, result.answer,
        )
        if not surfaced:
            logger.warning(
                "[conflict] model preferred the document but did not name the "
                "contradicted memory (discrepancy not surfaced)"
            )



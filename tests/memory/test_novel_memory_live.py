"""Live end-to-end memory tests over a real GraphRAG benchmark corpus.

Drives all three memory tiers — short-term (session working context), episodic
(the durable append-only log), and long-term (curated cross-session records) —
against the first novel corpus of the GraphRAG-Benchmark, ``Novel-30752``
("Vestiges of the Mayas"), and the 87 benchmark questions written for it.

The corpus text and questions are checked in as fixtures under ``tests/test_data/``
(regenerated by ``_regen_fixtures`` at the bottom of this file from
``ref_projects/GraphRAG-Benchmark/Datasets/``).

Nothing is faked: the LLM and embedder come from ``.env.yaml`` (see
``tests/live_setup``) and every store is a real persistent backend —
``SQLiteStructuredStore`` (a sqlite file), ``FAISSVectorStore`` (file-backed
FAISS), ``LocalFSLogStore`` and ``LocalFSDocumentStore`` (on-disk). The novel is
chunked, embedded, and upserted into FAISS so the query runner answers real
questions through ``vector_search``; the memory tiers ride on top.

Run with::

    pytest tests/memory/test_novel_memory_live.py -m live
"""


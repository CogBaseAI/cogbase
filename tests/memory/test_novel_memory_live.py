def _select_questions(questions: list[dict], n_per_type: int = _N_PER_TYPE) -> list[dict]:
    """First ``_N_PER_TYPE`` questions per type (file order), skipping skip-types."""
    by_type: dict[str, list[dict]] = {}
    selected: list[dict] = []
    for q in questions:
        qt = q.get("question_type", "Unknown")
        if qt in _SKIP_TYPES:
            continue
        bucket = by_type.setdefault(qt, [])
        if len(bucket) < n_per_type:
            bucket.append(q)
            selected.append(q)
    return selected


def _loads_lenient(content: str):
    """Parse the first JSON value in *content*, tolerating fences / trailing prose."""
    text = (content or "").strip()
    if text.startswith("```"):
        text = text.strip("`")
    start = min((i for i in (text.find("{"), text.find("[")) if i != -1), default=-1)
    if start == -1:
        raise json.JSONDecodeError("no JSON value found", text, 0)
    obj, _ = json.JSONDecoder().raw_decode(text[start:])
    return obj


def _fbeta_score(tp: int, fp: int, fn: int, beta: float = 1.0) -> float:
    """F-beta over statement classification counts (matches the benchmark)."""
    precision = tp / (tp + fp + 1e-10)
    recall = tp / (tp + fn + 1e-10)
    return (1 + beta**2) * (precision * recall) / ((beta**2 * precision) + recall + 1e-10)


async def _generate_statements(question: str, text: str) -> list[str]:
    """Decompose *text* into atomic, pronoun-free factual statements."""
    prompt = _STATEMENT_GENERATOR_PROMPT.format(question=question, answer=text)
    result = await _llm.complete([{"role": "user", "content": prompt}], model="mini")
    try:
        parsed = _loads_lenient(result.get("content") or "")
    except (json.JSONDecodeError, TypeError, ValueError):
        return [text] if text.strip() else []
    if isinstance(parsed, list):
        return [str(x) for x in parsed]
    if isinstance(parsed, dict):
        for key in ("statements", "answers", "items", "list", "output", "result"):
            value = parsed.get(key)
            if isinstance(value, list):
                return [str(x) for x in value]
        return [str(v) for v in parsed.values()]
    return [str(parsed)]


async def _calculate_factuality(
    question: str, answer_stmts: list[str], gt_stmts: list[str], beta: float
) -> tuple[float, tuple[int, int, int]]:
    """Classify answer/ground-truth statements into TP/FP/FN → F-beta score."""
    if not answer_stmts and not gt_stmts:
        return 1.0, (0, 0, 0)
    examples = "\n".join(
        f"Input: {json.dumps(ex['input'])}\nOutput: {json.dumps(ex['output'])}"
        for ex in _CORRECTNESS_EXAMPLES
    )
    prompt = _CORRECTNESS_PROMPT_TEMPLATE.format(
        examples=examples,
        question=question,
        answer=json.dumps(answer_stmts),
        ground_truth=json.dumps(gt_stmts),
    )
    result = await _llm.complete([{"role": "user", "content": prompt}], model="mini")
    try:
        parsed = _loads_lenient(result.get("content") or "")
        tp = len(parsed.get("TP", []))
        fp = len(parsed.get("FP", []))
        fn = len(parsed.get("FN", []))
    except (json.JSONDecodeError, TypeError, ValueError, AttributeError):
        return 0.0, (0, 0, 0)
    return _fbeta_score(tp, fp, fn, beta), (tp, fp, fn)


async def _semantic_similarity(answer: str, ground_truth: str) -> float:
    """Cosine similarity of the two embeddings, scaled to [0, 1] (deterministic)."""
    a_embed, gt_embed = await _embedder.embed([answer, ground_truth])
    dot = sum(x * y for x, y in zip(a_embed, gt_embed))
    na = math.sqrt(sum(x * x for x in a_embed))
    nb = math.sqrt(sum(y * y for y in gt_embed))
    if na == 0 or nb == 0:
        return 0.0
    return (dot / (na * nb) + 1) / 2


async def _judge_answer(question: str, ground_truth: str, generated: str) -> tuple[float, str]:
    """Score *generated* against *ground_truth* with the GraphRAG-Benchmark metric:
    weighted blend of statement-level F-beta factuality and embedding similarity.
    """
    w_fact, w_sim = _CORRECTNESS_WEIGHTS
    answer_stmts, gt_stmts = await asyncio.gather(
        _generate_statements(question, generated),
        _generate_statements(question, ground_truth),
    )
    factuality, (tp, fp, fn) = (
        await _calculate_factuality(question, answer_stmts, gt_stmts, _CORRECTNESS_BETA)
        if w_fact != 0 else (0.0, (0, 0, 0))
    )
    similarity = await _semantic_similarity(generated, ground_truth) if w_sim != 0 else 0.0
    score = w_fact * factuality + w_sim * similarity
    explanation = (
        f"TP={tp} FP={fp} FN={fn} factuality={factuality:.3f} "
        f"similarity={similarity:.3f} (w={w_fact:.2f}/{w_sim:.2f})"
    )
    return max(0.0, min(1.0, score)), explanation


async def _run_and_score(runner: QueryRunner, questions: list[dict], *, label: str) -> list[dict]:
    """Answer each question through *runner*, grade it, and log per-question detail.

    Questions are answered+graded concurrently (bounded by ``_EVAL_CONCURRENCY``);
    the returned rows preserve input order. Rows use the same field names as the
    GraphRAG eval harness so the written artifacts are consumable by
    ``llm_answer_accuracy.py stats``.
    """
    sem = asyncio.Semaphore(_EVAL_CONCURRENCY)
    total = len(questions)

    async def _one(i: int, q: dict) -> dict:
        async with sem:
            result = await _query_result(runner, q["question"])
            answer = result.answer
            score, explanation = await _judge_answer(q["question"], q["answer"], answer)
        row = {
            "id": q["id"],
            "question_type": q.get("question_type", "Unknown"),
            "question": q["question"],
            "ground_truth": q["answer"],
            "generated_answer": answer,
            "llm_score": score,
            "llm_explanation": explanation,
            # Every reference the answer drew on (QueryResult fields).
            "num_structured_records": len(result.structured_records),
            "structured_records": result.structured_records,
            "num_chunks": len(result.chunks),
            "chunks": [c.text for c in result.chunks],
            "num_document_slices": len(result.document_slices),
            "document_slices": [s.text for s in result.document_slices],
            "num_memory_refs": len(result.memories),
            "memory_refs": [m.content for m in result.memories],
            # Token accounting across all LLM calls for this query.
            "input_tokens": result.input_tokens,
            "output_tokens": result.output_tokens,
        }
        logger.info(
            "[answer-eval:%s] %d/%d type=%s score=%.2f "
            "refs(struct=%d chunks=%d slices=%d mem=%d) tokens(in=%d out=%d)"
            "\n  Q: %s\n  GT: %s\n  A: %s\n  why: %s",
            label, i, total, q.get("question_type"), score,
            len(result.structured_records), len(result.chunks),
            len(result.document_slices), len(result.memories),
            result.input_tokens, result.output_tokens,
            q["question"], q["answer"], answer, explanation,
        )
        return row

    return await asyncio.gather(*(_one(i, q) for i, q in enumerate(questions, 1)))


def _avg_by_type(rows: list[dict]) -> dict[str, float]:
    by_type: dict[str, list[float]] = {}
    for r in rows:
        by_type.setdefault(r["question_type"], []).append(r["llm_score"])
    return {qt: sum(v) / len(v) for qt, v in by_type.items()}


def _overall(rows: list[dict]) -> float:
    return sum(r["llm_score"] for r in rows) / len(rows) if rows else 0.0


def _format_stats(label: str, rows: list[dict]) -> str:
    """Render the per-type + overall averages for *rows* as a text block."""
    avgs = _avg_by_type(rows)
    lines = [f"{label}:"]
    for qt in sorted(avgs):
        n = sum(1 for r in rows if r["question_type"] == qt)
        lines.append(f"    {qt:<22} avg={avgs[qt]:.4f}  n={n}")
    lines.append(f"    {'OVERALL':<22} avg={_overall(rows):.4f}  n={len(rows)}")
    return "\n".join(lines)


def _format_reference_stats(label: str, rows: list[dict]) -> str:
    """Render how often answers cited each kind of reference, plus token usage.

    Covers every reference QueryResult carries — structured_records, chunks,
    document_slices, and memories. Per-type and overall: for each kind the share
    of queries that drew on at least one such reference and the average count per
    query, followed by the average input/output tokens per query.
    """
    # The reference kinds and the per-row count field each maps to.
    _KINDS = [
        ("structured", "num_structured_records"),
        ("chunks", "num_chunks"),
        ("slices", "num_document_slices"),
        ("memory", "num_memory_refs"),
    ]

    by_type: dict[str, list[dict]] = {}
    for r in rows:
        by_type.setdefault(r["question_type"], []).append(r)

    def _line(name: str, group: list[dict]) -> str:
        n = len(group)
        parts = [f"    {name:<22} n={n}"]
        for kind, field in _KINDS:
            counts = [r.get(field, 0) for r in group]
            with_refs = sum(1 for c in counts if c > 0)
            avg = sum(counts) / n if n else 0.0
            parts.append(f"{kind}={with_refs}/{n}(avg={avg:.2f})")
        avg_in = sum(r.get("input_tokens", 0) for r in group) / n if n else 0.0
        avg_out = sum(r.get("output_tokens", 0) for r in group) / n if n else 0.0
        parts.append(f"tokens(in={avg_in:.0f} out={avg_out:.0f})")
        return "  ".join(parts)

    lines = [f"{label} — query references & token usage:"]
    for qt in sorted(by_type):
        lines.append(_line(qt, by_type[qt]))
    lines.append(_line("OVERALL", rows))
    return "\n".join(lines)


def _log_stats(label: str, rows: list[dict]) -> dict[str, float]:
    block = _format_stats(f"[answer-eval] {label}", rows)
    logger.info(block)
    return _avg_by_type(rows)


async def _memory_records(lt: LongTermMemory) -> list[dict]:
    """All stored long-term records (any status), for dumping into the report."""
    return await lt._structured.query(lt._structured_collection)  # noqa: SLF001


def _format_memories(label: str, records: list[dict]) -> str:
    """Render *records* as a readable block keyed by status/kind/content."""
    lines = [f"{label} — {len(records)} memory record(s):"]
    for r in records:
        lines.append(
            f"    [{r.get('status')}] {r.get('kind')}: {r.get('content')}"
        )
    return "\n".join(lines)



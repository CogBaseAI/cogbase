def _load_questions() -> list[dict]:
    return json.loads(_QUESTIONS_FILE.read_text(encoding="utf-8"))


def _find_question(questions: list[dict], needle: str) -> dict:
    """Return the benchmark question whose text contains *needle* (case-insensitive)."""
    needle = needle.lower()
    for q in questions:
        if needle in q["question"].lower():
            return q
    raise AssertionError(f"no benchmark question contains {needle!r}")


class _NovelEnv:
    """Bundle of the persistent stores + memory tiers for one test session."""

    def __init__(self, *, vector, structured, log, docs, episodic, short_term,
                 long_term, vector_schema):
        self.vector = vector
        self.structured = structured
        self.log = log
        self.docs = docs
        self.episodic = episodic
        self.short_term = short_term
        self.long_term = long_term
        self.vector_schema = vector_schema

    def runner(self, *, with_long_term=False, long_term=None) -> QueryRunner:
        """Build a runner over the ingested corpus.

        ``with_long_term`` wires this env's own long-term store; ``long_term``
        overrides it with any other store (used to compare freshly-distilled
        memory stores against the no-memory baseline).
        """
        lt = long_term if long_term is not None else (self.long_term if with_long_term else None)
        return QueryRunner(
            app_id=_APP_ID,
            llm=_llm,
            resources=RetrievalResources(
                document_store=self.docs,
                vector_store=self.vector,
                embedder=_embedder,
                vector_schemas=[self.vector_schema],
            ),
            memory=MemoryTiers(
                short_term=self.short_term,
                episodic=self.episodic,
                long_term=lt,
            ),
        )


async def _build_and_ingest(persist: Path) -> _NovelEnv:
    """Build every persistent store and ingest the novel into FAISS (once)."""
    corpus = _CORPUS_FILE.read_text(encoding="utf-8")

    vector = FAISSVectorStore(path=persist / "novel_faiss")
    structured = SQLiteStructuredStore(persist / "novel.sqlite")
    log = LocalFSLogStore(persist / "logs")
    docs = LocalFSDocumentStore(persist / "docs")

    episodic = EpisodicMemory(log)
    short_term = ShortTermMemory(episodic=episodic, llm=_llm)

    # Long-term rides on its own app-scoped persistent stores (sqlite + faiss).
    long_term = LongTermMemory(
        SQLiteStructuredStore(persist / "long_term.sqlite").with_scope(AppScope(app_id=_APP_ID)),
        FAISSVectorStore(path=persist / "long_term_faiss").with_scope(AppScope(app_id=_APP_ID)),
        _llm,
        _embedder,
        app_id=_APP_ID,
    )

    # Chunk → embed → upsert the novel into a real FAISS collection.
    chunks = build_recursive_chunker(chunk_size=1024, overlap=128).chunk(
        Document(doc_id="Novel-30752", text=corpus)
    )
    embeddings = await _embedder.embed([c.text for c in chunks])
    dims = len(embeddings[0])

    vector_schema = VectorCollectionSchema(
        name=_PASSAGES,
        dimensions=dims,
        description="Passages from the novel 'Vestiges of the Mayas' (Novel-30752).",
    )
    env = _NovelEnv(
        vector=vector, structured=structured, log=log, docs=docs,
        episodic=episodic, short_term=short_term, long_term=long_term,
        vector_schema=vector_schema,
    )
    await vector.create_collection(env.vector_schema)
    await vector.upsert(
        _PASSAGES,
        [c.model_copy(update={"embedding": e}) for c, e in zip(chunks, embeddings)],
    )
    logger.info("[novel] ingested %d passages, dims=%d", len(chunks), dims)
    return env


@pytest.fixture(scope="module")
def env(tmp_path_factory) -> _NovelEnv:
    """Module-scoped: build the stores and ingest the corpus once.

    Built on a throwaway loop (the stores are loop-agnostic — sqlite is sync,
    FAISS/localfs use ``get_event_loop`` at call time), then reused by each
    function-scoped async test.
    """
    persist = tmp_path_factory.mktemp("novel_memory")
    return asyncio.run(_build_and_ingest(persist))


async def _query_result(runner: QueryRunner, text: str, *, session_id: str | None = None) -> QueryResult:
    result: QueryResult | None = None
    async for item in runner.run(text, base_prompt=_QUERY_PROMPT, session_id=session_id):
        if isinstance(item, QueryResult):
            result = item
    assert result is not None, f"runner produced no QueryResult for {text!r}"
    return result


async def _query(runner: QueryRunner, text: str, *, session_id: str | None = None) -> str:
    return (await _query_result(runner, text, session_id=session_id)).answer



# Long-term memory ↔ answer correctness: does distilled memory move the score?
#
# Measures how much long-term memory helps end-to-end answer accuracy on the
# real benchmark questions, judged by an LLM grader (mirrors
# benchmarks/graphrag/llm_evaluation/llm_answer_accuracy.py).
#
# The distiller only promotes a subject-matter fact when the USER is its source
# (an assertion, correction, or confirmation) — not when the assistant merely
# retrieved and restated it from the corpus, which the source documents already
# own (see cogbase/memory/distill.py).  This harness exercises that rule from
# both sides:
#
#   baseline   — runner with no long-term tier (corpus retrieval only).
#   control    — seed the *assistant's own* retrieved answers as ASSISTANT turns
#                and distil.  Provenance-negative: the rule should decline these,
#                so almost no durable fact is promoted.  Distilled, not re-asked.
#   treatment  — seed the *ground-truth* facts as USER assertions ("remember
#                this …") and distil.  Provenance-positive: the rule should
#                promote these user-sourced facts; accept them at the review gate
#                and re-ask, so recall can lift the score.
#
# Note on the review gate: in production only PREFERENCE / RETRIEVAL_HINT
# auto-promote to ``active``; FACT / CORRECTION wait at ``pending_review`` and
# are *not* recalled.  A distilled subject-matter fact is almost always a FACT,
# so the treatment drives the real review gate — the same ``list_pending`` /
# ``review_many`` APIs the REST endpoints (``list_pending_memories`` /
# ``review_memories``) expose — to accept the gated facts after distillation,
# measuring the lift once a reviewer has approved them, not advocating that
# production auto-activate them.
# ---------------------------------------------------------------------------

# Slice: first N questions of each type, in file order.  "Creative Generation"
# is dropped (a single question — no meaningful per-type average).
_N_PER_TYPE = 5
_SKIP_TYPES = frozenset({"Creative Generation", "Fact Retrieval"})
# Cap on questions answered+graded concurrently per run, to bound load on the
# real LLM/embedder without serializing the whole slice.
_EVAL_CONCURRENCY = 5

# Answer-correctness scoring follows the GraphRAG-Benchmark
# (``ref_projects/GraphRAG-Benchmark/Evaluation/metrics/answer_accuracy.py``):
# decompose answer + ground truth into atomic statements, classify them
# TP/FP/FN for an F-beta *factuality* score, and blend with a deterministic
# embedding cosine *similarity*.  This is far more stable run-to-run than a
# single holistic 0-1 grade, and matches the metric the README leaderboard
# numbers are scored against.
_CORRECTNESS_WEIGHTS = (0.75, 0.25)  # (factuality, semantic similarity)
_CORRECTNESS_BETA = 1.0


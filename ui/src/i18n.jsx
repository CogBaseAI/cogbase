// Walk a dot-namespaced key into a dictionary; returns undefined if absent.
function lookup(dict, key) {
  return key.split('.').reduce((o, k) => (o == null ? undefined : o[k]), dict)
}

function interpolate(str, vars) {
  if (!vars) return str
  return str.replace(/\{(\w+)\}/g, (m, k) => (vars[k] != null ? String(vars[k]) : m))
}

const I18nCtx = createContext(null)

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState(detectInitialLang)

  const setLang = useCallback((code) => {
    setLangState(code)
    try { localStorage.setItem(STORAGE_KEY, code) } catch {}
  }, [])

  // Translate `key`, interpolating `{vars}`. Falls back to English, then the
  // raw key, so a missing translation degrades gracefully instead of crashing.
  const t = useCallback((key, vars) => {
    const hit = lookup(translations[lang], key) ?? lookup(translations.en, key)
    return typeof hit === 'string' ? interpolate(hit, vars) : key
  }, [lang])

  return (
    <I18nCtx.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nCtx.Provider>
  )
}

export function useT() {
  const ctx = useContext(I18nCtx)
  if (!ctx) throw new Error('useT must be used within an I18nProvider')
  return ctx
}
